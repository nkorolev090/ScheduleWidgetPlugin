package com.example.schedule_plugins;

public class ScheduleColor {
    private int mainColor;
    private int lecColor;
    private int semColor;
    private int labColor;
    private int timeColor;

    public int getLabColor() {
        return labColor;
    }

    public int getLecColor() {
        return lecColor;
    }

    public int getMainColor() {
        return mainColor;
    }

    public int getSemColor() {
        return semColor;
    }

    public int getTimeColor() {
        return timeColor;
    }

    public void setLabColor(int labColor) {
        this.labColor = labColor;
    }

    public void setLecColor(int lecColor) {
        this.lecColor = lecColor;
    }

    public void setMainColor(int mainColor) {
        this.mainColor = mainColor;
    }

    public void setSemColor(int semColor) {
        this.semColor = semColor;
    }

    public void setTimeColor(int timeColor) {
        this.timeColor = timeColor;
    }
}
